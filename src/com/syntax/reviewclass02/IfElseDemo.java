public class IfElseDemo {

	public static void main(String[] args) {
		
		boolean flag=false;
		
		if(flag) {
			System.out.println("only displayed when if is true");
		} else {
			System.out.println("only displayed when if is false");
		}
		
		if(10>15) {
			System.out.println("true");
		} else {
			System.out.println("false");
		}

	}

}
