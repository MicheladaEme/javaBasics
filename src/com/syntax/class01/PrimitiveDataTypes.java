package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		
		// 1 create a variable=box=container
		
		/*
		 * to create a variable we need:
		 * 1. specify dataType
		 * 2. give a name
		 */
		
		//datatypes to represent numeric values
		byte box1=10;
		
		short box2=20;
		
		int box3=30; //most used
		
		long box4=40; //will be using
		
		//datatypes to represent decimals
		
		float price=12.99F;
		
		double price1=13.99; //most used
		
		//to represent a single character
		
		char gender='F';
		
		char sign='$';
		
		//to represent yes or no
		
		boolean snow=false;
		
		boolean tired=true;
		
		//I want to print 13.99 ???
		System.out.println(price1);

		System.out.println(price);
		
		System.out.println(box1);
		
		System.out.println(box2);
		
		System.out.println(box4);
		
		System.out.println(gender);
		
		System.out.println(sign);
		
		System.out.println(snow);
		
		System.out.println(tired);
	}

}
